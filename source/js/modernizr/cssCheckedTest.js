/* eslint no-undef: "off" */
Modernizr.addTest('csschecked', () => Modernizr.testStyles('#modernizr input {margin-left:0px;} #modernizr input:checked {margin-left: 20px;}', (elem) => {
    const chx = document.createElement('input');
    chx.type = 'checkbox';
    chx.checked = 'checked';
    elem.appendChild(chx);
    return elem.lastChild.offsetLeft >= 20;
}));
